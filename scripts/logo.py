np.random.seed(0)
n = 10000
x = np.random.uniform(-29, 29, n)
y = np.random.uniform(-6, 6, n)
j = ((x + 29) // 1).astype(int)
i = ((y + 6) // 1).astype(int)
x, y = x[mask[i, j]], y[mask[i, j]]

# Generate a set of fibers
mat = Mat(len(x), length=1, width=0.1, thickness=0.05, size=60, tensile=400)
mat.x = x
mat.y = -y
# Build the fiber network
net = Net(mat, periodic=False)
# Stack fibers
stack = Stack(mat, net)
# Create the fiber mesh
mesh = Mesh(stack)

# Solve the mechanical packing problem
K, C, u, f, F, H, Z, rlambda, mask, err = solver(
    mat, mesh,
    packing=4, itermax=10000, lmin=0.01, coupling=0.99, interp_size=100
)

# Export as VTK
vtk = vtk_mesh(mat, mesh,
               *u(1).reshape(-1, 2).T,
               *(f(1) @ C).reshape(-1, 2).T)
vtk.plot(scalars="force", cmap=plt.cm.twilight_shifted)
vtk.save("outputs/vtk.vtk")
